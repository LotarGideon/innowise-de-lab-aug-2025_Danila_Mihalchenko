# Описание: Создайте функцию без параметров show_current_time() — печатает текущие дату и время
from datetime import datetime
def show_current_time(): # Создаём функцию
    now = datetime.now() # Локальная переменная для хранения даты и времени
    print(f"Текущие дата и время: {now}")
show_current_time() # Вызов функции
