--Часть3. Задача 1. Подсчёт количества клиентов в каждой стране
SELECT
	country,	-- Столбец с названием страны проживания клиента
	count(customer_id)	-- Агрегатная функция для подсчёта количества клиентов в каждой из стран
FROM customers	-- Таблица-источник (покупатели)
GROUP BY country;	-- Группировка по странам
