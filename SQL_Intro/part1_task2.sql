-- Часть 1. Задача 2. Заказы, сумма которых больше 1000
SELECT	-- Ключевое слово для выборки данных
    order_id,        -- ID заказа
    item,            -- Наименование товара
    amount,          -- Сумма заказа
    customer_id      -- ID клиента
FROM orders          -- Таблицу-источник "orders" (заказы)
WHERE amount > 1000; -- Условие фильтрации: только заказы с суммой больше 1000