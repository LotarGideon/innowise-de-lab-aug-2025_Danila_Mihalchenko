--Часть 3. Задача 2. Общее количество заказов и средняя сумма по каждому товару
SELECT
	item,	-- Название товара
	count(order_id),	-- Агрегатная функция для подсчёта количества заказов по каждому товару
	avg(amount) AS avg_amount	-- Агрегатная функция для нахождения средней стоимости по каждому товару
FROM orders	-- Таблица-источник (заказы)
GROUP BY item;	-- Группировка по товарам