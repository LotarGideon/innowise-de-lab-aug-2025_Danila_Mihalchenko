-- Часть 1. Задача 1. Клиенты из USA, возраст которых больше 25
SELECT	-- Ключевое слово для поиска
    first_name,    -- Столбец с именем клиента
    last_name,     -- Столбец с фамилией клиента  
    age,           -- Столбец с возрастом клиента
    country        -- Столбец со страной клиента
FROM customers     -- Таблица-источник данных 
WHERE country = 'USA'  -- Условие фильтрации: только клиенты из США
AND age > 25;      -- Дополнительное условие: возраст больше 25 лет















